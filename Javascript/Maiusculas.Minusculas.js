// Transforme letras minusculas em maiusculas. Faca o contrario disso tambem.

let word = "AAAAAA!"
console.log(word.toLowerCase()) // Nesse caso transformamos a string AAAAAA em letra minuscula.


let palavra = "Amo Programar"
console.log(palavra.toUpperCase())  // Nesse caso transformamos a string Amo Programar em letra maiuscula. 

// OBS: Todo metodo (toUpperCase ou toLowerCase, como nos exemplos acima) sao acompanhados de ()
