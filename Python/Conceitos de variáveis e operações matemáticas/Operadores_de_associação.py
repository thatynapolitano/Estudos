# O que são? 

# São operadores utilizados para verificar se um objeto está presente em uma sequência. 

curso = "Curso de Python"
frutas = ["laranja", "uva", "limão"]	
saques = [1500, 100]

pergunta = "Python" in curso
print(pergunta)

pergunta = "mação" not in frutas
print(pergunta)

pergunta = 200 in saques
print(pergunta)

